int snapshot_create_time_stamp(char *source_frame, int frmae_width, int frame_height,const char *time_stamp, bool is_YUV422 )
{
		int source_width=frmae_width;
		int source_height=frame_height;
		int map_width = MAP_WIDTH_16;
		int map_height= MAP_HEIGHT_16;
		int gap_between_map = GAP_BETWEEN_MAP ;
		int i,j,loop=0;
		unsigned char timestamp_char;
		const char* timestamp_str=time_stamp;
		const unsigned char* timestamp_map_p=NULL;
		int current_xoffset = 0;
		int current_yoffset = 0;
		int time_stamp_width =0;
		
	
		
		LOGV("snapshot_create_time_stamp datetime=%s, source_width=%d, frame_height=%d\n",
			timestamp_str, source_width, source_height);
		if(frmae_width<640 || frame_height<480)
		{
			LOGE("sorry,the resolution is too small\n");
			return 1;			
		}
		if(!is_YUV422)
		{
			LOGE("sorry,the date format should be YUV422\n");
			return 1;
		}
		if ((NULL == timestamp_str) || (strlen(timestamp_str) > TIME_STAMP_LENGTH)) {
			LOGE("%s timestamp_str=NULL or len error!", __FUNCTION__);
			return 1;
		}
		if(!strcmp("offtime", timestamp_str))
		{
			LOGV("%s timestamp_str=%s !", __FUNCTION__, timestamp_str);
			return 1;		
		}
		

		if( source_width==0 ||  source_height ==0)
		{
			LOGE("%s frmae_width=%d, source_height=%d!", __FUNCTION__, frmae_width, source_height);
			return 1;
		}
		
        if(frmae_width>=1024)
        {
			map_width = MAP_WIDTH_32;
			map_height = MAP_HEIGHT_32;
        }

		time_stamp_width = TIME_STAMP_LENGTH*(map_width+1);

		current_xoffset = source_width - RIGHT_MARGIN - time_stamp_width;
		current_yoffset = source_height - BOTTOM_MARGIN - map_height;



	
		while((timestamp_char=*timestamp_str++)!=0x00)
		{

			LOGE("snapshot_create_time_stamp timestamp_char=0x%x,xoff=%d,yoff=%d\n",
					timestamp_char,current_xoffset ,current_yoffset);
			if(timestamp_char==' ')
			{
				current_xoffset+= GAP_BETWEEN_MAP+BK_SPACE_PIXELS;
				continue;
			}
			find_each_time_stamp_font_lib(timestamp_char, &timestamp_map_p,map_width);
			LOGE("snapshot_create_time_stamp timestamp_map_p=0x%x\n",(int)timestamp_map_p );	

			if(timestamp_map_p)
			{
				LOGE("snapshot_merge_date_stamp buffer, map_height=%d,map_width=%d\n",  map_height, map_width);
				for(i= 0;i<map_height;i++)
					for(j=0;j<map_width;j++)
				{
					if(*((uint8_t *)timestamp_map_p+i*map_width+j)==STAMP_NUM_MARGIN)
					{

						*((uint8_t *)source_frame+(current_yoffset+i)*source_width*2+(current_xoffset+j)*2)=STAMP_MARGIN_Y;
						//if((current_yoffset+i)%2==0)
						{
							if((current_xoffset+j)%2==0)
							{
								*((uint8_t *)source_frame+(current_yoffset+i)*source_width*2+(current_xoffset+j)*2+1)=STAMP_MARGIN_CB;
							}
							else
							{
								*((uint8_t *)source_frame+(current_yoffset+i)*source_width*2+(current_xoffset+j)*2+1)=STAMP_MARGIN_CR; 
							}
						}
					}
					else if(*((uint8_t *)timestamp_map_p+i*map_width+j)==STAMP_NUM_BODY)
					{
						
						*((uint8_t *)source_frame+(current_yoffset+i)*source_width*2+(current_xoffset+j)*2)=STAMP_BODY_Y; 
						//if((current_yoffset+i)%2==0)
						{
							if((current_xoffset+j)%2==0)
							{
								*((uint8_t *)source_frame+(current_yoffset+i)*source_width*2+(current_xoffset+j)*2+1)=STAMP_BODY_CB;
							}
							else
							{
								*((uint8_t *)source_frame+(current_yoffset+i)*source_width*2+(current_xoffset+j)*2+1)=STAMP_BODY_CR; 
							}
						}
					}
				}

				current_xoffset+=map_width+GAP_BETWEEN_MAP;
			}
			loop++;
			if(loop>25)
			{
				LOGE("%s break the data stamp loop for dead loop\n", __FUNCTION__);
				break;

			}
		}
		return 1;

return 1;
}

